def generate_causal_state_graph_pdf(path: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    repo_root = Path(__file__).resolve().parents[2]
    tex_source = repo_root / "paper" / "figures" / "causal_state_graph.tex"
    if not tex_source.is_file():
        write_causal_chain_figure_tex(tex_source)
    work_dir = path.parent
    build_copy = work_dir / "causal_state_graph_build.tex"
    build_copy.write_text(tex_source.read_text(encoding="utf-8"), encoding="utf-8")
    pdflatex = shutil.which("pdflatex")
    if pdflatex is None:
        if not path.exists():
            path.write_bytes(b"% PDF placeholder - run pdflatex on causal_state_graph.tex\n")
        return
    subprocess.run(
        [pdflatex, "-interaction=nonstopmode", "-halt-on-error", build_copy.name],
        cwd=work_dir,
        check=True,
        capture_output=True,
    )
    built = work_dir / "causal_state_graph_build.pdf"
    if built.is_file():
        built.replace(path)
    for suffix in (".aux", ".log"):
        extra = work_dir / f"causal_state_graph_build{suffix}"
        extra.unlink(missing_ok=True)


def pick_example_chain(trace: CausalStateTransitionTrace) -> str:
    for zone in trace.zone_ids:
        chain = build_explanation_chain(trace, zone)
        if "EVACUATION_CANDIDATE" in chain or "EXIT_IMPAIRMENT" in chain:
            return chain
    return canonical_escalation_chain_text()


