def _tex_identifier(value: str) -> str:
    return value.replace("_", r"\_")


def write_causal_metrics_tex(
    metrics_by_scenario: dict[str, CausalGraphMetrics],
    stability_by_scenario: dict[str, float],
    path: Path,
) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = [
        "% Auto-generated by dualexis CSSG export.",
        "",
        "\\begin{table}[htbp]",
        "  \\centering",
        "  \\caption{Causal Safety State Graph (CSSG) metrics by scenario "
        "(mean over seeds). CSSG extends SSSG with typed causal edges and root-cause paths.}",
        "  \\label{tab:causal-metrics}",
        "  \\small",
        "  \\begin{tabular}{@{}lrrrrrr@{}}",
        "    \\toprule",
        "    Scenario & Depth & Root prec. & Path compl. & Trace compl. & "
        "Trans. prec. & Stability \\\\",
        "    \\midrule",
    ]
    for scenario, metrics in sorted(metrics_by_scenario.items()):
        stability = stability_by_scenario.get(scenario, metrics.explanation_stability_across_seeds)
        lines.append(
            f"    {_tex_identifier(scenario)} & "
            f"{metrics.causal_explanation_depth:.2f} & "
            f"{metrics.root_cause_precision:.2f} & "
            f"{metrics.causal_path_completeness:.2f} & "
            f"{metrics.causal_trace_completeness:.2f} & "
            f"{metrics.transition_precision:.2f} & "
            f"{stability:.2f} \\\\"
        )
    lines.extend(["    \\bottomrule", "  \\end{tabular}", "\\end{table}", ""])
    path.write_text("\n".join(lines), encoding="utf-8")


def write_causal_reasoning_section(
    *,
    example_chain: str,
    path: Path,
) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    escaped_chain = example_chain.replace("\n", "\\\\\n    ")
    content = f"""\\subsection{{Causal Safety State Graph (CSSG)}}
\\label{{sec:causal-reasoning}}

DUALEXIS advances the Semantic Safety State Graph (SSSG) into a \\textbf{{Causal Safety State Graph
(CSSG)}}---a novel \\emph{{explainable safety reasoning}} framework. Unlike temporal event graphs
that only sequence observations, CSSG attributes each safety-state transition to typed causal
mechanisms with confidence, supporting evidence, and explicit alternative hypotheses.

\\paragraph{{Positioning.}}
Event-centric pipelines answer \\emph{{what happened}}; CSSG answers \\emph{{why the safety state
changed}} and \\emph{{which root factors warrant staff attention}}. Recommendations remain advisory;
CSSG supplies auditable causal chains for human disposition (Section~\\ref{{sec:governance-evaluation}}).

\\paragraph{{Typed causal edges.}}
CSSG edges are labelled \\texttt{{contributes\\_to}}, \\texttt{{aggravates}}, \\texttt{{mitigates}},
or \\texttt{{triggers}}, replacing generic ``causal'' arcs in SSSG. Each transition stores:
\\textbf{{causal\\_factors}}, \\textbf{{confidence}}, \\textbf{{supporting\\_evidence}}, and
\\textbf{{alternative\\_explanations}}.

\\paragraph{{Root-cause extraction.}}
The function \\texttt{{find\\_root\\_causes(zone\\_id, tick)}} walks backward along zone transitions
to surface primary \\texttt{{contributes\\_to}} / \\texttt{{triggers}} factors---the earliest
explainable drivers, not merely the latest alert.

\\paragraph{{Causal metrics.}}
Table~\\ref{{tab:causal-metrics}} reports \\textbf{{causal\\_explanation\\_depth}},
\\textbf{{root\\_cause\\_precision}}, \\textbf{{causal\\_path\\_completeness}}, and
\\textbf{{explanation\\_stability\\_across\\_seeds}} (Jaccard overlap of chain signatures under
seed perturbation).

\\paragraph{{Example explanation chain.}}
\\begin{{verbatim}}
{escaped_chain}
\\end{{verbatim}}

\\begin{{figure}}[htbp]
  \\centering
  \\includegraphics[width=0.92\\linewidth]{{figures/causal_state_graph.pdf}}
  \\caption{{Causal Safety State Graph (CSSG): typed causal edges (\\texttt{{contributes\\_to}},
            \\texttt{{aggravates}}, \\texttt{{triggers}}, \\texttt{{mitigates}}) over safety states.
            Dotted arcs indicate temporal progression; CSSG is a causal explanation framework, not an
            event timeline.}}
  \\label{{fig:causal-state-graph}}
\\end{{figure}}

\\input{{tables/causal_metrics}}
"""
    path.write_text(content, encoding="utf-8")


