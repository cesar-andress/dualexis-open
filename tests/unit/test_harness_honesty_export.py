@pytest.mark.unit
def test_privacy_fuzz_wrong_probe_count(tmp_path: Path) -> None:
    csv_path = tmp_path / "results.csv"
    csv_path.write_text(
        "case_id,pass\none,True\n",
        encoding="utf-8",
    )
    with pytest.raises(HarnessHonestyInputError, match="expected 10 probes"):
        load_privacy_fuzz_pass_rate(csv_path)


@pytest.mark.unit
def test_privacy_fuzz_pass_rate_all_pass() -> None:
    rows = FIXTURES / "privacy_fuzz_all_pass.csv"
    rows.write_text(
        "case_id,pass\n"
        + "\n".join(f"p{i},True" for i in range(10))
        + "\n",
        encoding="utf-8",
    )
    try:
        _, total, rate = load_privacy_fuzz_pass_rate(rows)
        assert total == 10
        assert rate == pytest.approx(1.0)
    finally:
        rows.unlink(missing_ok=True)


