@pytest.mark.unit
def test_render_latex_structure() -> None:
    metrics = HarnessHonestyMetrics(
        leakage_score=0.111,
        procedural_independence=1.0,
        distributional_independence=0.222,
        ground_truth_stability_mean=0.4967,
        privacy_fuzz_pass_rate=0.9,
        privacy_fuzz_probe_count=10,
        governance_compliance_score=0.44,
        decision_traceability=1.0,
        mean_path_trust=0.333,
    )
    tex = render_harness_honesty_latex(metrics)
    assert "\\label{tab:harness-honesty}" in tex
    assert tex.count("&") >= 7
    assert "\\toprule" in tex
    assert "\\bottomrule" in tex
    assert AUTO_GENERATED_BANNER.strip() in tex


