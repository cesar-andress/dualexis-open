"""Unit tests for harness honesty Table 7 export."""

from __future__ import annotations

import re
from pathlib import Path

import pytest

from dualexis.evaluation.harness_honesty_export import (
    AUTO_GENERATED_BANNER,
    HarnessHonestyInputError,
    HarnessHonestyMetrics,
    HarnessHonestyPaths,
    export_harness_honesty_tex,
    load_harness_honesty_metrics,
    load_privacy_fuzz_pass_rate,
    render_harness_honesty_latex,
)

FIXTURES = Path(__file__).resolve().parents[1] / "fixtures" / "harness_honesty"


@pytest.mark.unit
def test_load_metrics_happy_path() -> None:
    paths = HarnessHonestyPaths(
        leakage_report=FIXTURES / "leakage_audit_report.json",
        privacy_fuzz_csv=FIXTURES / "privacy_fuzz_results.csv",
        governance_report=FIXTURES / "governance_audit_report.json",
        trust_report=FIXTURES / "trust_propagation_report.json",
    )
    metrics = load_harness_honesty_metrics(paths)
    assert metrics.leakage_score == pytest.approx(0.111)
    assert metrics.distributional_independence == pytest.approx(0.222)
    assert metrics.governance_compliance_score == pytest.approx(0.44)
    assert metrics.mean_path_trust == pytest.approx(0.333)


